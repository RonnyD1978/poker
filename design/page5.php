<h2>Some quick rules &amp; notes to myself</h2>

<ul>
    <li>First create a few (empty) classes/interfaces to get a little startup code up and running.</li>
    <li>Start each session with one or more 'I want to test that...' lines</li>
    <li>Apply TDD (write failing test, write code, fix test, goto 10)</li>
    <li>Use factories to create several stages of the game to test with. Might not unit test these factories since i only need them to run something.</li>
    <li>Deliberately use either private or public methods (at least in first working version) to see if i can rely on good testable structure instead of OOP overkill.</li>
</ul>
